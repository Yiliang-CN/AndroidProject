package cn.gxust.project.Bean;

public class ShopBean {
    private int shopId;             // 商店id
    private String shopName;        // 商店名称
    private String shopSales;       // 商店销量
    private String shopPrice;       // 商店价格
    private int shopPhone;          // 商家电话
    private String shopAddress;     // 商店地址
    private String shopImg;         // 商店图片

    public int getShopId() {
        return shopId;
    }

    public void setShopId(int shopId) {
        this.shopId = shopId;
    }

    public String getShopName() {
        return shopName;
    }

    public void setShopName(String shopName) {
        this.shopName = shopName;
    }

    public String getShopSales() {
        return shopSales;
    }

    public void setShopSales(String shopSales) {
        this.shopSales = shopSales;
    }

    public String getShopPrice() {
        return shopPrice;
    }

    public void setShopPrice(String shopPrice) {
        this.shopPrice = shopPrice;
    }

    public int getShopPhone() {
        return shopPhone;
    }

    public void setShopPhone(int shopPhone) {
        this.shopPhone = shopPhone;
    }

    public String getShopAddress() {
        return shopAddress;
    }

    public void setShopAddress(String shopAddress) {
        this.shopAddress = shopAddress;
    }

    public String getShopImg() {
        return shopImg;
    }

    public void setShopImg(String shopImg) {
        this.shopImg = shopImg;
    }

}
